#include "libft.h"

/*
** Swap using arithmetic operations.
*/

void	ft_aswap(int *n1, int *n2)
{
	*n1 = *n1 + *n2;
	*n2 = *n1 - *n2;
	*n1 = *n1 - *n2;
}
