#include <stdio.h>

void	ft_prime_factor(int n)
{
	int		i;

	if (n == 1)
	{
		printf("1");
		return ;
	}
	i = 2;
	while (n / i != 1)
	{
		if (n % i == 0)
		{
			printf("%d*", i);
			n /= i;
			i--;
		}
		i++;
	}
	printf("%d", n);
}
